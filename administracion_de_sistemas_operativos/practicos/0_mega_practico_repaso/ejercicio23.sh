# Empaquetar el directorio:
tar -cvf archivos.tar /home/usuario/archivos/

# Ver el contenido del archivo empaquetado:
tar -tvf archivos.tar

# Desempaquetar el archivo:
tar -xvf archivos.tar

# Empaquetar y comprimir el directorio:
tar -czvf archivos.tar.gz /home/usuario/archivos/

# Ver el contenido del archivo comprimido:
tar -tzvf archivos.tar.gz

# Desempaquetar y descomprimir el archivo:
tar -xzvf archivos.tar.gz